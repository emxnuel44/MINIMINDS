// Función para obtener emoji por categoría y nombre
export const getElementEmoji = (categoria, nombre) => {
  switch (categoria) {
    case 'Números':
      return NUMBER_EMOJIS[nombre] || '🔢';
    case 'Letras':
      return LETTER_EMOJIS[nombre] || '🔤';
    case 'Animales':
      return ANIMAL_EMOJIS[nombre] || '🐶';
    case 'Colores':
      return COLOR_EMOJIS[nombre] || '🎨';
    case 'Cuerpo':
      return BODY_EMOJIS[nombre] || '🧍';
    case 'Comida':
      return FOOD_EMOJIS[nombre] || '🍎';
    case 'Sílabas':
      return SYLLABLE_EMOJIS[nombre] || '📝';
    case 'Partes del Hogar':
      return HOME_EMOJIS[nombre] || '🏠';
    case 'Objetos':
      return OBJECT_EMOJIS[nombre] || '📦';
    case 'Miembros de la Familia':
      return FAMILY_EMOJIS[nombre] || '👨‍👩‍👧‍👦';
    case 'Climas':
      return WEATHER_EMOJIS[nombre] || '🌤️';
    case 'Horas':
      return TIME_EMOJIS[nombre] || '🕐';
    case 'Buenos Modales':
      return MANNERS_EMOJIS[nombre] || '🙏';
    default:
      return '❓';
  }
};
